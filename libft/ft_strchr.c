#include "./libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	cc;

	cc = (char)c;
	i = 0;
	while (i <= ft_strlen(s))
	{
		if (s[i] == cc)
			return ((char *)&s[i]);
		i++;
	}
	return (0);
}
